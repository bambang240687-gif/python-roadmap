# operasi_aritmatika_lengkap (Pythonic)
x = 10
y = 7

print(f"Hasil Penjumlahan: {x + y}")
print(f"Hasil Pengurangan: {x - y}")
print(f"Hasil Perkalian: {x * y}")
print(f"Hasil Pembagian: {x / y}")
print(f"Hasil Modulus: {x % y}")
print(f"Hasil Pangkat: {x ** y}")
print(f"Hasil Floor Division: {x // y}")
print(f"Hasil Bitwise XOR: {x ^ y}")
print(f"Hasil Bitwise AND: {x & y}")
print(f"Hasil Bitwise OR: {x | y}")
